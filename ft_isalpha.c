int ft_isalpha(int c)
{
    return ((c >= 'A' && c <= 'Z')
     || (c >= 'a' && c <= 'z'));
}

#include <stdio.h>
int main()
{
    char c = 'a';
    printf("%d", ft_isalpha(c));
}